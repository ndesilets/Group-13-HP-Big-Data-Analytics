CREATE OR REPLACE VIEW CAPSTONE_DEMO.PGA_EXPERIMENT_SUMMARY_V AS

SELECT
  A.*,
  B.WORK_AREA_SIZE_GB,
  B.EXPECTED_SIZE_GB,
  B.ACTUAL_MEM_USED_GB,
  B.MAX_MEM_USED_GB,
  B.TEMPSEG_SIZE_GB,
  C.BUFFER_GETS_GB,
  C.IO_INTERCONNECT_GB,
  C.PHYSICAL_READ_GB,
  C.PHYSICAL_WRITE_GB,
  C.USER_IO_WAIT_TIME_SEC
FROM CAPSTONE_DEMO.TIME_USAGE_SUMMARY_V A
  INNER JOIN CAPSTONE_DEMO.WORK_AREA_SUMMARY_V B
    ON A.SQL_EXEC_START = B.SQL_EXEC_START
  INNER JOIN CAPSTONE_DEMO.IO_SUMMARY_V C
    ON A.SQL_EXEC_START = C.SQL_EXEC_START;

SELECT
  *
FROM CAPSTONE_DEMO.PGA_EXPERIMENT_SUMMARY_V
WHERE MODULE = 'DB_BIG_TABLE_CACHE_EXPERIMENT'
ORDER BY
  SQL_EXEC_START;